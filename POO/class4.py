class calcuradora():
    def __init__(self):
        pass
    def somar(self, a, b):
        return a + b
    def subtrair(self, a, b):
        return a - b
    def multiplicar(self, a, b):
        return a * b
    def dividir(self, a, b):
        return a / b
calcuradora = calcuradora()
print(calcuradora.somar(5, 3))
print(calcuradora.subtrair(10, 4))
print(calcuradora.multiplicar(7, 6))
print(calcuradora.dividir(9, 19))          